#hash
teams = {
    "Barcelona" => "Spain",
    "Liverpool" => "England",
    "Bayern" => "Germany",
    "Juventus" => "Italy"
}

puts teams["Barcelona"]          #Spain
puts teams.size                  #4
puts teams.empty?                #false
puts teams.keys                  #["Barcelona", "Liverpool", "Bayern", "Juventus"]
puts teams.values                #["Spain", "England", "Germany", "Italy"]

